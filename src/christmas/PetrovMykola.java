public class PetrovMykola {
    public static final String ANSI_BLUE = "\u001B[34m";
    public static final String ANSI_RESET = "\u001B[0m";
    public static void main(String[] args){
        System.out.println(ANSI_BLUE + "              -----" + ANSI_RESET);
        System.out.println(ANSI_BLUE + "             /     \\" + ANSI_RESET);
        System.out.println(ANSI_BLUE + "            |       |" + ANSI_RESET);
        System.out.println(ANSI_BLUE + "                   /" + ANSI_RESET);
        System.out.println(ANSI_BLUE + "              -----" + ANSI_RESET);
        System.out.println(ANSI_BLUE + "              | $ |" + ANSI_RESET);
        System.out.println(ANSI_BLUE + "          $ $ $ * $ $ $" + ANSI_RESET);
        System.out.println(ANSI_BLUE + "      $ $ * * * * * * * $ $" + ANSI_RESET);
        System.out.println(ANSI_BLUE + "    $ x x x x x x x x x x x $" + ANSI_RESET);
        System.out.println(ANSI_BLUE + "    $ x x x x x x x x x x x $" + ANSI_RESET);
        System.out.println(ANSI_BLUE + "  $ * * * * * * * * * * * * * $" + ANSI_RESET);
        System.out.println(ANSI_BLUE + "  $ * * * * * * * * * * * * * $" + ANSI_RESET);
        System.out.println(ANSI_BLUE + "  $ x x x x x x x x x x x x x $" + ANSI_RESET);
        System.out.println(ANSI_BLUE + "$ x x x x x x x x x x x x x x x $" + ANSI_RESET);
        System.out.println(ANSI_BLUE + "  $ * * * * * * * * * * * * * $" + ANSI_RESET);
        System.out.println(ANSI_BLUE + "  $ * * * * * * * * * * * * * $" + ANSI_RESET);
        System.out.println(ANSI_BLUE + "  $ x x x x x x x x x x x x x $" + ANSI_RESET);
        System.out.println(ANSI_BLUE + "    $ x x x x x x x x x x x $" + ANSI_RESET);
        System.out.println(ANSI_BLUE + "      $ * * * * * * * * * $" + ANSI_RESET);
        System.out.println(ANSI_BLUE + "        $ $ * * * * * $ $" + ANSI_RESET);
        System.out.println(ANSI_BLUE + "          $ $ $ x $ $ $" + ANSI_RESET);
        System.out.println(ANSI_BLUE + "                $" + ANSI_RESET);
    }
}
    

